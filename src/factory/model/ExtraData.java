package factory.model;

/**
 * @author Gulya
 */
public class ExtraData {

    private String field_name;
    private String field_type;
    private String field_value;
    private String field_lvl;
    private String field_sub_lvl;
    private String field_id;

    /**
     * @return the field_name
     */
    public String getField_name() {
        return field_name;
    }

    /**
     * @param field_name the field_name to set
     */
    public void setField_name(String field_name) {
        this.field_name = field_name;
    }

    /**
     * @return the field_type
     */
    public String getField_type() {
        return field_type;
    }

    /**
     * @param field_type the field_type to set
     */
    public void setField_type(String field_type) {
        this.field_type = field_type;
    }

    /**
     * @return the field_value
     */
    public String getField_value() {
        return field_value;
    }

    /**
     * @param field_value the field_value to set
     */
    public void setField_value(String field_value) {
        this.field_value = field_value;
    }

    /**
     * @return the field_lvl
     */
    public String getField_lvl() {
        return field_lvl;
    }

    /**
     * @param field_lvl the field_lvl to set
     */
    public void setField_lvl(String field_lvl) {
        this.field_lvl = field_lvl;
    }

    /**
     * @return the field_id
     */
    public String getField_id() {
        return field_id;
    }

    /**
     * @param field_id the field_id to set
     */
    public void setField_id(String field_id) {
        this.field_id = field_id;
    }

    /**
     * @return the field_sub_lvl
     */
    public String getField_sub_lvl() {
        return field_sub_lvl;
    }

    /**
     * @param field_sub_lvl the field_sub_lvl to set
     */
    public void setField_sub_lvl(String field_sub_lvl) {
        this.field_sub_lvl = field_sub_lvl;
    }


}
